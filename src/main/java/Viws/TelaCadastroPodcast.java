/**valida todos os campos
 * 
 * @return 
 */
    private boolean validaCampos(){
        
        
     String produtor = txtProdutor.getText();
     String numeroEp = ftxtNumeroEp.getText();
     String duracao = ftxtDuracao.getText();
     
     boolean valido = true; 

//     valida se os campos estão preenchidos
     if(txtProdutor.getText().trim().isEmpty() || txtNomeEp.getText().trim().isEmpty() || ftxtNumeroEp.getText().trim().isEmpty() 
             || ftxtDuracao.getText().trim().isEmpty() || txtUrl.getText().trim().isEmpty()){
        
         JOptionPane.showMessageDialog(rootPane, "Todos os campos devem ser preenchidos. ");
         
     } else{
         
         boolean strProdutor = produtor.matches("[a-zA-Z]{1,150}");
         boolean strNumero = numeroEp.matches("[0-9]{1,3}");
         boolean strDuracao = duracao.matches("[0-9]{2}[:][0-9]{2}");
         
        //     valida se os campos estão preenchidos corretamente 
        if(strProdutor == false && strNumero == false && strDuracao == false){
         
            JOptionPane.showMessageDialog(rootPane, """
                                                    O campo Produtor  deve conter apenas letras.
                                                    O campo Nº do episódio deve conter apenas. 
                                                    O campo Duração deve ser no formato (00:00).""");
          
        } else if(strProdutor == false){
         
          JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! O campo Produtor  deve conter apenas letras.");
          
        } else if(strNumero == false){
         
          JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! O campo Nº do episódio deve conter apenas. ");
         
        } else if(strDuracao == false){
         
          JOptionPane.showMessageDialog(rootPane, "ATENÇÃO! O campo Duração deve ser no formato (00:00). ");
         
        } else{
         
         valido = false; // se naõ estiver preenchido retorna false
        }
     }
     
     return valido;  //  se estiver preenchido retorna true
     
    }
    
    
