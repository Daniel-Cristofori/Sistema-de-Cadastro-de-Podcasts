    /**botão para catdastrar novo podcast
     * 
     * @param evt 
     */
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       
        if(!validaCampos()) {
            
            Podcast podcast = new Podcast();
            
            podcast.setProdutor(txtProdutor.getText());
            podcast.setNome_episodio(txtNomeEp.getText());
            podcast.setNumero_episodio(Integer.parseInt(ftxtNumeroEp.getText()));
            podcast.setDuracao(ftxtDuracao.getText());
            podcast.setUrl_repositorio(txtUrl.getText());
            
            PodcastDAO podcastDAO = new PodcastDAO();
            
            podcastDAO.cadastrar(podcast);
            
            
            txtProdutor.setText("");
            txtNomeEp.setText("");
            ftxtNumeroEp.setText("");
            ftxtDuracao.setText("");
            txtUrl.setText("");
            
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

